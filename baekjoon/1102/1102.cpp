#include <vector>

int _count_bit(const int& mask, const int& size)
{
    int count = 0;
    for (int i = 0; i < size; i++) {
        if ((1 << i) & mask)
            count++;
    }
    return count;
}

void solution()
{
    int N;
    cin >> N;

    vector<vector<int>> e(N, vector<int>(N, 0));

    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            cin >> e[i][j];
        }
    }

    string status;
    int init_mask = 0;
    int target;
    cin >> status;
    cin >> target;

    int init_count = 0;

    for (size_t i = 0; i < status.size(); i++) {
        if (status[i] == 'Y') {
            init_mask |= (1 << i);
            init_count++;
        }
    }

    if (target == 0) {
        cout << 0;
        return;
    }

    if (init_count >= target) {
        cout << 0;
        return;
    }

    if (init_count == 0) {
        cout << -1;
        return;
    }

    int max_set = (1 << N);
    vector<int> dp(max_set, C_MAX);

    dp[init_mask] = 0;

    for (int mask = 0; mask < max_set; mask++) {
        if (dp[mask] == C_MAX)
            continue;

        int count = _count_bit(mask, N);
        if (count >= target)
            continue;

        for (int i = 0; i < N; i++) {
            if (!((1 << i) & mask))
                continue;
            for (int j = 0; j < N; j++) {
                if (((1 << j) & mask))
                    continue;
                int next_mask = mask | (1 << j);
                dp[next_mask] = min(dp[next_mask], (dp[mask] + e[i][j]));
            }
        }
    }

    int answer = C_MAX;
    for (int i = 0; i < max_set; i++) {
        if (_count_bit(i, N) >= target) {
            answer = min(answer, dp[i]);
        }
    }

    if (answer == C_MAX) {
        cout << -1;
    } else {
        cout << answer;
    }
}

