void solution(){
    int n;
    int blu_ray;

    cin >> n;
    cin >> blu_ray;

    vector<int> list(n);

    int sum_of_list = 0;

    for (int i = 0; i < n; i++){
        cin >> list[i];
        sum_of_list += list[i];
    }

    {
        int left = list[list.size() - 1], right = sum_of_list, middle, answer = sum_of_list;

        while (left <= right) {
            middle = (left + right) / 2;

            int c_blu_ray = 1;
            int remain = middle;

            for (int i = 0; i < n; i++){
                if (remain < list[i]){
                    c_blu_ray++;
                    remain = middle;
                }

                remain -= list[i];
            }

            if (c_blu_ray > blu_ray){
                left = middle + 1;
            } else if (c_blu_ray < blu_ray) {
                right = middle - 1;
            } else {
                answer = middle;
                right = middle - 1;
            }
        }

        cout << answer;
    }
}
























































